`timescale 1ns/1ps

module regfile_tb;

    reg clk;
    reg WE3;

    reg [4:0] A1;
    reg [4:0] A2;
    reg [4:0] A3;

    reg [31:0] WD3;

    wire [31:0] RD1;
    wire [31:0] RD2;

    // DUT
    regfile dut (
        .clk(clk),
        .WE3(WE3),
        .A1(A1),
        .A2(A2),
        .A3(A3),
        .WD3(WD3),
        .RD1(RD1),
        .RD2(RD2)
    );

    // Clock generation
    always #5 clk = ~clk;

    initial begin

        //-----------------------------------
        // Initialize
        //-----------------------------------
        clk = 0;
        WE3 = 0;
        A1  = 0;
        A2  = 0;
        A3  = 0;
        WD3 = 0;

        //-----------------------------------
        // Write x1 = 10
        //-----------------------------------
        #10;
        WE3 = 1;
        A3  = 5'd1;
        WD3 = 32'd10;

        #10;

        //-----------------------------------
        // Write x2 = 20
        //-----------------------------------
        A3  = 5'd2;
        WD3 = 32'd20;

        #10;

        //-----------------------------------
        // Stop writing
        //-----------------------------------
        WE3 = 0;

        //-----------------------------------
        // Read x1 and x2
        //-----------------------------------
        A1 = 5'd1;
        A2 = 5'd2;

        #5;

        $display("--------------------------------");
        $display("x1 = %0d", RD1);
        $display("x2 = %0d", RD2);
        $display("--------------------------------");

        //-----------------------------------
        // Attempt write x0 = 999
        //-----------------------------------
        WE3 = 1;
        A3  = 5'd0;
        WD3 = 32'd999;

        #10;

        WE3 = 0;

        //-----------------------------------
        // Read x0
        //-----------------------------------
        A1 = 5'd0;

        #5;

        $display("x0 = %0d", RD1);

        //-----------------------------------
        // Finish
        //-----------------------------------
        #10;
        $finish;

    end

endmodule